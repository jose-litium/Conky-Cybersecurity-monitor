check_rkhunter() {
    echo -e "${BLUE}Running manual RKHunter scan...${NC}"
    local warn_log="/var/log/rkhunter_warnings.txt"
    
    # Run update and check
    if sudo /usr/local/bin/conky-rkhunter-wrapper.sh --update >/dev/null 2>&1 && \
       sudo /usr/local/bin/conky-rkhunter-wrapper.sh --propupd >/dev/null 2>&1; then
        
        local result_file
        result_file="$(mktemp)"
        
        if sudo /usr/local/bin/conky-rkhunter-wrapper.sh --check --sk > "$result_file" 2>/dev/null; then
            grep -iE "(warning|alert|suspect)" "$result_file" > "$warn_log" 2>/dev/null || true
            rm -f "$result_file"
            
            if [[ -s "$warn_log" ]]; then
                local count
                count="$(wc -l < "$warn_log")"
                dmsg "Scan complete: $count alert(s) found.\nView with option 12 or: cat $warn_log"
                log "RKHunter manual scan: $count warnings found"
            else
                dmsg "Scan complete: No alerts detected."
                log "RKHunter manual scan: Clean"
                rm -f "$warn_log"
            fi
        else
            dmsg "RKHunter check failed. Check logs for details."
            log "ERROR: RKHunter check command failed"
            return 1
        fi
    else
        dmsg "RKHunter update failed. Ensure you have internet connectivity."
        log "ERROR: RKHunter update failed"
        return 1
    fi
}

restore_system() {
    if ! dconfirm "Restore essential system packages? This will reinstall core desktop components."; then
        dmsg "System restoration canceled by user."
        return 1
    fi
    
    echo -e "${BLUE}Restoring essential system packages...${NC}"
    if [[ -z "$APT_HAS_UPDATED" ]]; then
        sudo apt update
        APT_HAS_UPDATED=1
    fi
    
    local -a essential_packages=(
        "ubuntu-desktop"
        "gdm3"
        "nautilus"
        "gvfs"
        "gvfs-backends"
        "network-manager"
    )
    
    local restored=0 skipped=0
    local -a packages_to_install=()
    
    for pkg in "${essential_packages[@]}"; do
        if dconfirm "Reinstall $pkg?"; then
            packages_to_install+=("$pkg")
        else
            ((skipped++)) || true
            log "Skipped package: $pkg"
        fi
    done

    if [[ ${#packages_to_install[@]} -gt 0 ]]; then
        if sudo apt install -y "${packages_to_install[@]}"; then
            for pkg in "${packages_to_install[@]}"; do
                ((restored++)) || true
                log "Restored package: $pkg"
            done
        else
            for pkg in "${packages_to_install[@]}"; do
                log "ERROR: Failed to restore $pkg"
            done
        fi
    fi
    
    # Fix broken dependencies if requested
    if dconfirm "Run 'sudo apt --fix-broken install -y' to fix dependency issues?"; then
        if sudo apt --fix-broken install -y; then
            log "Fixed broken dependencies"
        else
            log "WARNING: Could not fix all broken dependencies"
        fi
    fi
    
    dmsg "Restoration complete.\nRestored: $restored | Skipped: $skipped"
    log "System restoration batch complete"
}

