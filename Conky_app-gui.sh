uninstall_conky() {
    if ! dconfirm "Are you sure you want to completely remove Conky and all its components?"; then
        dmsg "Uninstallation canceled by user."
        return 1
    fi
    
    echo -e "${RED}Stopping Conky and related services...${NC}"
    
    # Stop user services
    systemctl --user stop conky.service 2>/dev/null || true
    systemctl --user disable conky.service 2>/dev/null || true
    rm -f ~/.config/systemd/user/conky.service
    
    systemctl --user stop temp_monitor.service 2>/dev/null || true
    systemctl --user disable temp_monitor.service 2>/dev/null || true
    rm -f ~/.config/systemd/user/temp_monitor.service
    
    systemctl --user daemon-reload 2>/dev/null || true
    
    # Kill any remaining conky processes
    pkill -f "conky.*conky.conf" 2>/dev/null || true
    sleep 2
    
    echo -e "${RED}Removing configuration files...${NC}"
    rm -rf ~/.config/conky
    rm -rf "$INSTALL_DIR"
    
    echo -e "${GREEN}Removing packages...${NC}"
    sudo apt remove --purge -y conky conky-all 2>/dev/null || true
    sudo apt autoremove -y 2>/dev/null || true
    
    # Remove sudoers entry and wrapper script
    sudo rm -f /etc/sudoers.d/conky-monitor 2>/dev/null || true
    sudo rm -f /usr/local/bin/conky-rkhunter-wrapper.sh 2>/dev/null || true
    
    # Remove system-level RKHunter services
    remove_rkhunter_service 2>/dev/null || true
    
    dmsg "Conky uninstallation completed. Temporary files can be cleaned separately."
    log "Conky completely uninstalled."
}

