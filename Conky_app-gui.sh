setup_sudoers() {
    # Validate and escape USER to prevent Command Injection / Privilege Escalation
    if [[ ! "$USER" =~ ^[a-zA-Z0-9_.-]+$ ]]; then
        log "ERROR: Invalid USER variable format."
        return 1
    fi
    local safe_user
    safe_user="$(printf '%q' "$USER")"

    local sudoers_file="/etc/sudoers.d/conky-monitor"
    local wrapper_script="/usr/local/bin/conky-rkhunter-wrapper.sh"

    # Create wrapper script for rkhunter
    sudo tee "$wrapper_script" > /dev/null <<'EOF'
#!/usr/bin/env bash
set -euo pipefail

# Secure wrapper for rkhunter to prevent complex argument matching in sudoers
case "${1:-}" in
    --update)
        exec /usr/bin/rkhunter --update
        ;;
    --propupd)
        exec /usr/bin/rkhunter --propupd
        ;;
    --check)
        if [[ "${2:-}" == "--sk" && -z "${3:-}" ]]; then
            exec /usr/bin/rkhunter --check --sk
        else
            echo "Invalid arguments" >&2
            exit 1
        fi
        ;;
    *)
        echo "Invalid arguments" >&2
        exit 1
        ;;
esac
EOF
    sudo chmod 755 "$wrapper_script"
    
    # Create secure sudoers entry with minimal required permissions
    sudo tee "$sudoers_file" > /dev/null <<EOF
# Conky Cybersecurity Monitor - Minimal sudo permissions
# Generated by conky-cybersecurity-monitor.sh

# Allow user to run the specific wrapper script without password
${safe_user} ALL=(ALL) NOPASSWD: /usr/local/bin/conky-rkhunter-wrapper.sh
EOF
    
    # Set secure permissions
    sudo chmod 0440 "$sudoers_file"
    
    # Validate sudoers syntax before applying
    if sudo visudo -c -f "$sudoers_file" &>/dev/null; then
        log "Sudoers configuration validated and applied for user $USER."
    else
        log "ERROR: Sudoers syntax validation failed. Rolling back."
        sudo rm -f "$sudoers_file"
        return 1
    fi
}

