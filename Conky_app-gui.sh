clean_var_log() {
    if dconfirm "Clean /var/log? This will vacuum journal logs to 1 day and truncate log files."; then
        # Safe log cleaning with error handling
        if sudo journalctl --vacuum-time=1d 2>/dev/null; then
            log "Journal vacuumed to 1 day"
        fi
        
        # Only truncate files that are safe to truncate (avoid critical system logs)
        local -a safe_logs=(
            "/var/log/apt/history.log"
            "/var/log/apt/term.log"
            "/var/log/bootstrap.log"
            "/var/log/dpkg.log"
        )
        
        for logfile in "${safe_logs[@]}"; do
            if [[ -f "$logfile" && -w "$logfile" ]]; then
                sudo truncate -s 0 "$logfile" 2>/dev/null || true
            fi
        done
        
        dmsg "/var/log system logs cleaned safely."
        log "/var/log cleaning completed."
    else
        dmsg "Cleaning /var/log canceled by user."
        log "/var/log cleaning canceled."
    fi
}

