}

check_install() {
    local pkgs=("$@")
    local missing_pkgs=()

    for pkg in "${pkgs[@]}"; do
        if dpkg-query -W -f='${Status}' "$pkg" 2>/dev/null | grep -q " ok installed"; then
            log "$pkg is already installed."
        else
            log "$pkg is not installed."
            missing_pkgs+=("$pkg")
        fi
    done

    if [[ ${#missing_pkgs[@]} -eq 0 ]]; then
        return 0
    fi

    local pkg_list="${missing_pkgs[*]}"
    if dconfirm "Do you want to install the following missing packages?\n$pkg_list"; then
        if [[ -z "$APT_HAS_UPDATED" ]]; then
            sudo apt update
            APT_HAS_UPDATED=1
        fi
        if sudo apt install -y "${missing_pkgs[@]}"; then
            log "Successfully installed packages: $pkg_list"
            return 0
        else
            log "ERROR: Failed to install packages: $pkg_list"
            dmsg "Failed to install packages. This may affect functionality."
            return 1
        fi
    else
        dmsg "Skipped installing packages: $pkg_list. This may affect functionality."
        return 1
    fi
}

configure_sensors() {
    echo -e "${BLUE}Configuring hardware sensors (lm-sensors)...${NC}"
    if command -v sensors &>/dev/null; then
        if dconfirm "Do you want to run sensors-detect to automatically configure your sensors?"; then
            echo -e "${BLUE}Running sensors-detect (auto-accept defaults)...${NC}"
            # Use --auto to avoid interactive prompts, redirect output to log
            if yes | sudo sensors-detect &>> "$LOGFILE"; then
                # Load detected modules
                sudo modprobe -a coretemp it87 2>/dev/null || true
                dmsg "Sensors configuration completed."
                log "Sensors configured successfully."
            else
                log "WARNING: sensors-detect completed with warnings"
                dmsg "Sensors configuration completed with warnings. Check logs."
            fi
        else
            dmsg "Skipped configuration with sensors-detect."
            log "Sensors-detect skipped by user."
        fi
    else
        dmsg "lm-sensors is not installed. Skipping sensor configuration."
        log "lm-sensors not available, skipping sensor config."
    fi
}

create_rkhunter_scan_script() {
    local script_path="$INSTALL_DIR/rkhunter_scan.sh"
    cat <<'EOF' > "$script_path"
#!/usr/bin/env bash
set -euo pipefail

# Secure RKHunter scan script for Conky Monitor
# Outputs warnings to a temp file for Conky to display

readonly INSTALL_DIR="$HOME/.local/conky_app"
readonly WARN_FILE="/var/log/rkhunter_warnings.txt"
readonly RESULT_FILE="$(mktemp "$INSTALL_DIR/rkhunter_result.XXXXXX")"

cleanup() {
    rm -f "$RESULT_FILE" 2>/dev/null || true
}
trap cleanup EXIT

# Update RKHunter databases (silent)
sudo /usr/local/bin/conky-rkhunter-wrapper.sh --update >/dev/null 2>&1 || true
sudo /usr/local/bin/conky-rkhunter-wrapper.sh --propupd >/dev/null 2>&1 || true

# Run check and capture output
if sudo /usr/local/bin/conky-rkhunter-wrapper.sh --check --sk > "$RESULT_FILE" 2>/dev/null; then
    # Extract only warning lines for Conky display
    grep -iE "(warning|alert|suspect)" "$RESULT_FILE" > "$WARN_FILE" 2>/dev/null || true
    
    # Create comparison file for change detection
    if [[ -f "/var/log/rkhunter_warnings_prev.txt" ]]; then
        if ! cmp -s "$WARN_FILE" "/var/log/rkhunter_warnings_prev.txt"; then
            cp "$WARN_FILE" "/var/log/rkhunter_warnings_prev.txt"
            echo "CHANGED" > "/var/log/rkhunter_status.txt"
        else
            echo "UNCHANGED" > "/var/log/rkhunter_status.txt"
        fi
    else
        cp "$WARN_FILE" "/var/log/rkhunter_warnings_prev.txt" 2>/dev/null || true
        echo "INITIAL" > "/var/log/rkhunter_status.txt"
    fi
fi

# Ensure file exists even if empty (for Conky to read)
touch "$WARN_FILE"
EOF
    chmod 700 "$script_path"
    log "RKHunter scan script created at $script_path with secure permissions."
}

create_temp_monitor_script() {
    local script_path="$INSTALL_DIR/temp_monitor.sh"
    cat <<'EOF' > "$script_path"
#!/usr/bin/env bash
set -euo pipefail

# CPU Temperature Monitor for Conky
# Writes safe, sanitized output to temp file

readonly INSTALL_DIR="$HOME/.local/conky_app"
readonly TEMP_FILE="$INSTALL_DIR/cpu_temp.txt"
readonly MAX_SAFE_TEMP=90

cleanup() {
    rm -f "$TEMP_FILE" 2>/dev/null || true
}
trap cleanup EXIT

while true; do
    # Safely extract temperature value
    local temp_raw
    temp_raw="$(sensors 2>/dev/null | grep -Eo 'Package id 0:.*\+[0-9.]+' | head -n1 | grep -Eo '\+[0-9.]+' | tr -d '+' || echo "")"
    
    if [[ -n "$temp_raw" ]]; then
        # Validate it's a number
        if [[ "$temp_raw" =~ ^[0-9]+(\.[0-9]+)?$ ]]; then
            local temp_val
            temp_val="$(printf "%.1f" "$temp_raw")"
            # Cap at MAX_SAFE_TEMP to avoid display issues
            local tmp_file
            tmp_file="$(mktemp "$INSTALL_DIR/cpu_temp.XXXXXX")"
            if awk "BEGIN {exit !($temp_val > $MAX_SAFE_TEMP)}"; then
                echo ">${MAX_SAFE_TEMP}°C" > "$tmp_file"
            else
                echo "${temp_val}°C" > "$tmp_file"
            fi
            mv "$tmp_file" "$TEMP_FILE"
        else
            local tmp_file
            tmp_file="$(mktemp "$INSTALL_DIR/cpu_temp.XXXXXX")"
            echo "N/A" > "$tmp_file"
            mv "$tmp_file" "$TEMP_FILE"
        fi
    else
        local tmp_file
        tmp_file="$(mktemp "$INSTALL_DIR/cpu_temp.XXXXXX")"
        echo "N/A" > "$tmp_file"
        mv "$tmp_file" "$TEMP_FILE"
    fi
    sleep 10
done
EOF
    chmod 700 "$script_path"
    log "Temperature monitor script created at $script_path with secure permissions."
}

