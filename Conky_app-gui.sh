########################################
# Dependency Check Function (Hardened)
########################################

check_dependencies() {
    declare -A required_commands=(
        ["dialog"]="dialog"
        ["conky"]="conky-all"
        ["curl"]="curl"
        ["netstat"]="net-tools"
        ["lsof"]="lsof"
        ["xdg-open"]="xdg-utils"
        ["rkhunter"]="rkhunter"
        ["sensors"]="lm-sensors"
        ["nmap"]="nmap"
        ["upower"]="upower"
    )
    
    local -a missing_dependencies=()
    
    for cmd in "${!required_commands[@]}"; do
        if ! is_command_installed "$cmd"; then
            missing_dependencies+=("${required_commands[$cmd]}")
        fi
    done

    if [[ ${#missing_dependencies[@]} -gt 0 ]]; then
        echo -e "${RED}Faltan las siguientes dependencias:${NC}" >&2
        for dep in "${missing_dependencies[@]}"; do
            echo "- $dep" >&2
        done
        echo -e "${YELLOW}Por favor, instala las dependencias que faltan usando el gestor de paquetes de tu sistema.${NC}" >&2

        if is_command_installed "apt"; then
            echo "Ejemplo para Debian/Ubuntu: sudo apt install ${missing_dependencies[*]}" >&2
        elif is_command_installed "dnf"; then
            echo "Ejemplo para Fedora: sudo dnf install ${missing_dependencies[*]}" >&2
        elif is_command_installed "yum"; then
            echo "Ejemplo para RHEL/CentOS: sudo yum install ${missing_dependencies[*]}" >&2
        elif is_command_installed "pacman"; then
            echo "Ejemplo para Arch: sudo pacman -S ${missing_dependencies[*]}" >&2
        else
            echo "No se detectó un gestor de paquetes común. Instala manualmente las dependencias." >&2
        fi
        
        log "ERROR: Missing dependencies: ${missing_dependencies[*]}"
        exit 1
    fi
    log "All dependencies satisfied."
}

