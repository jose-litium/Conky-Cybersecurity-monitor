install_rkhunter_service() {
    echo -e "${BLUE}Installing automatic RKHunter scan service...${NC}"
    
    # Validate and escape USER to prevent Command Injection / Privilege Escalation
    if [[ ! "$USER" =~ ^[a-zA-Z0-9_.-]+$ ]]; then
        log "ERROR: Invalid USER variable format."
        return 1
    fi
    local safe_user
    safe_user="$(printf '%q' "$USER")"

    # System-level service for RKHunter (requires sudo)
    sudo tee /etc/systemd/system/rkhunter-auto.service > /dev/null <<'EOL'
[Unit]
Description=RKHunter Automatic Security Scan for Conky Monitor
After=network-online.target
Wants=network-online.target

[Service]
Type=oneshot
ExecStart=/usr/local/bin/rkhunter-auto-scan.sh
RemainAfterExit=yes
PrivateTmp=true
ProtectSystem=strict
ReadWritePaths=/var/log

[Install]
WantedBy=multi-user.target
EOL

    # Create the actual scan script with proper permissions
    sudo mkdir -p /usr/local/bin
    sudo tee /usr/local/bin/rkhunter-auto-scan.sh > /dev/null <<EOL
#!/usr/bin/env bash
set -euo pipefail
# Automated RKHunter scan wrapper for systemd

readonly WARN_FILE="/var/log/rkhunter_warnings.txt"
readonly RESULT_FILE="\$(mktemp)"
readonly CONKY_USER="${safe_user}"

cleanup() { rm -f "\$RESULT_FILE" 2>/dev/null || true; }
trap cleanup EXIT

/usr/bin/rkhunter --update >/dev/null 2>&1 || true
/usr/bin/rkhunter --propupd >/dev/null 2>&1 || true
/usr/bin/rkhunter --check --sk > "\$RESULT_FILE" 2>/dev/null || true

# Extract warnings for Conky
grep -iE "(warning|alert|suspect)" "\$RESULT_FILE" > "\$WARN_FILE" 2>/dev/null || touch "\$WARN_FILE"
chown "\${CONKY_USER}:\${CONKY_USER}" "\$WARN_FILE" 2>/dev/null || true
chmod 644 "\$WARN_FILE" 2>/dev/null || true
exit 0
EOL
    sudo chmod 750 /usr/local/bin/rkhunter-auto-scan.sh

    # Timer unit for periodic execution
    sudo tee /etc/systemd/system/rkhunter-auto.timer > /dev/null <<'EOL'
[Unit]
Description=Timer for RKHunter automatic security scans
Requires=rkhunter-auto.service

[Timer]
OnBootSec=10min
OnUnitActiveSec=6h
RandomizedDelaySec=30min
Persistent=true

[Install]
WantedBy=timers.target
EOL

    sudo systemctl daemon-reload
    sudo systemctl enable --now rkhunter-auto.timer 2>/dev/null || {
        log "WARNING: Could not enable rkhunter-auto.timer"
        return 1
    }
    log "RKHunter service and timer installed with hardened configuration."
}

remove_rkhunter_service() {
    echo -e "${RED}Removing automatic RKHunter scan service and timer...${NC}"
    
    # Stop and disable timer first
    sudo systemctl disable --now rkhunter-auto.timer 2>/dev/null || true
    sudo rm -f /etc/systemd/system/rkhunter-auto.timer
    
    # Stop and disable service
    sudo systemctl disable --now rkhunter-auto.service 2>/dev/null || true
    sudo rm -f /etc/systemd/system/rkhunter-auto.service
    
    # Remove helper script
    sudo rm -f /usr/local/bin/rkhunter-auto-scan.sh
    
    sudo systemctl daemon-reload
    log "RKHunter service, timer, and helper script removed."
}

